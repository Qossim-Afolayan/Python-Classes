#Boolean; a data type that can either be true or false (George Boole)
print(True)
print(False)

#equality operator and inequality operators return Boolean
print(5 == 5)
print(5 == 8)
print(8.3 == 8.3)
print(5 == "5")

print("ham" == "ham")
#case sensitivity
print("ham" == "Ham")
print("" == "")
print("!##" == "!##")
print("What's peanut butter without", end=" ")
print("Jelly? NOTHING!")

print(5 == 5.0)
print(5 == 5.1)
print(True == True)
print(True == False)

#inequality
print("Ham" != "ham")
print(10 != "10")
